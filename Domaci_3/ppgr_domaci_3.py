def Euler2A(phi, theta, psi):
  Rx_phi = Rodrigez(np.array([1, 0, 0]), phi)
  Ry_theta = Rodrigez(np.array([0, 1, 0]), theta)
  Rz_psi = Rodrigez(np.array([0, 0, 1]), psi)

  return np.matmul(np.matmul(Rz_psi, Ry_theta), Rx_phi)
